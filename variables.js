// variables:
// js code has a memory in the browser
// dynammic typed language.
// variables - 8 byte (64-bits)

// var & let values can be Manipulated.

// var
// can access outside the block
var amount = 1000;
console.log("Your Bill amount is Rs. " + amount)

// let
// can only access inside the block
let age = 18;
console.log(age)

// constant 
// constant are fixed values and cannot be Manipulated.
const pi = 3.14;
console.log(pi)
