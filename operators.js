
// pre Increment 
let b = 20
console.log(b)  // 20
console.log(++b) // 21
console.log(b)  // 21

console.log("------------")

// pre decrement 
let y = 40
console.log(y)  // 40
console.log(--y) // 39
console.log(y)  // 39

console.log("------------")

// post increment
let a = 10
console.log(a++)  // 10
console.log(a)    // 11

console.log("------------")

// post decrement
let x = 30
console.log(x--)  // 30
console.log(x)    // 29

console.log("------------")

let P = 5;
let Q = P++;
console.log(P)  // 6
console.log(Q)  // 5

// comparison Operator
console.log("------------")

let n1 = "10";
console.log(typeof(n1))
console.log(n1 == 10)  // true
console.log(n1 === 10) // false
console.log(n1 === '10') // true
